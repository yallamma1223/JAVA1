package com.Operators;

public class Unary {

	public static void main(String[] args) {
//increment decrement ++ --
		int a=12;
		System.out.println(a);//12
		System.out.println(++a);//13
		System.out.println(a++);//13
		System.out.println(a);//14
		System.out.println(--a);//13
		System.out.println(a--);//13
		System.out.println(a);//12

		
	}

}
